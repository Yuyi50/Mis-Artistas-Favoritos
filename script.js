function leon() {
    let name = prompt("¿Quién es el?")
    let nombre = document.getElementById (nombre1)
    nombre1.textContent = name

    let gener = prompt("¿Qué genero interpreta?")
    let genero = document.getElementById (genero1)
    genero1.textContent = gener

    let country = prompt("¿De donde es?")
    let pais = document.getElementById (pais1)
    pais1.textContent = country

    let song = prompt("Escribe una cancion de el")
    let cancion = document.getElementById (cancion1)
    cancion1.textContent = song
} 
